<?php

    $x = 10;
    echo "++$x is =" . (++$x) . "\n"; // Outputs: 11
echo "<br>";
    echo "Now x is =" . $x . "\n";   // Outputs: 11
echo "<br>";
    
    $x = 10;
    echo "$x++ is =" . ($x++) . "\n"; // Outputs: 10
echo "<br>";
    echo "Now x is =" . $x . "\n";   // Outputs: 11
echo "<br>";
    
    $x = 10;
    echo "--$x is =" . (--$x) . "\n"; // Outputs: 9
echo "<br>";
    echo "Now x is =" . $x . "\n";   // Outputs: 9
    echo "<br>";
    $x = 10;
    echo "$x-- is =" . ($x--) . "\n";// Outputs: 10
echo "<br>";
    echo "Now x is =" . $x;   // Outputs: 9

